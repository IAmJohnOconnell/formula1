export const getDriverFlag = (photos, nationality) => {
  if (!nationality) {
    console.warn("Location country not provided");
    return null;
  }
  const normalizedNationality = nationality.split(" ").join("");

  const flagMap = createDriverFlagMap(photos);
  return flagMap[normalizedNationality] || null;
};

export const getCircuitPhoto = (photos, locationCountry) => {
  if (!locationCountry) {
    console.warn("Location country not provided");
    return null;
  }

  const normalizedCountry = locationCountry
    .replace("grand prix", "")
    .toLowerCase()
    .split(" ")
    .join("");

  const circuitMap = createCircuitMapping();

  if (!circuitMap[normalizedCountry]) {
    console.warn(`No mapping found for country: ${normalizedCountry}`);
    return null;
  }

  const circuitKey = circuitMap[normalizedCountry].circuitKey;
  const circuitPhoto = photos.circuit[circuitKey];

  if (!circuitPhoto) {
    console.warn(`No photo found for ${circuitKey}`);
    return null;
  }

  return circuitPhoto;
};

